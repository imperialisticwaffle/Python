# We can use input functions to create mad libs.
print("Roses are {color}\n{plural noun} are blue\nI love {celebrity}")

# Next we define these variables above the mad libs text.
color = input("Enter a colour: ")
plural_noun = input("Enter a plural noun: ")
celebrity = input("Enter a celebrity's name: ")
print("Roses are " + color + ",\n" + plural_noun + " are blue,\nI love " + celebrity)